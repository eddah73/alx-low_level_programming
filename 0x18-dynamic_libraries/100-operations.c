#include <stdio.h>
#include "main.h"

/**
 * add - adds 2 numbers
 * sub - subtract 2 numbers
 * mul - multiply 2 integers
 * div - divides 2 intergers
 * mod - divides 2 numbers and return its remainder
 * @a: int type number
 * @b: int type number
 * Return: result of multiplication
 */

int add(int a, int b)
{
	return (a + b);
}

int sub(int a, int b)
{
	return (a - b);
}

int mul(int a, int b)
{
	return (a * b);
}

int div(int a, int b)
{
	return (a / b);
}

int mod(int a, int b)
{
	return (a % b);
}
